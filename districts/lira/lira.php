<script>
    // Add any custom JavaScript here
    document.addEventListener('DOMContentLoaded', function() {
        // Activate tooltips
        var tooltipTriggerList = [].slice.call(document.querySelectorAll('[data-bs-toggle="tooltip"]'))
        var tooltipList = tooltipTriggerList.map(function(tooltipTriggerEl) {
            return new bootstrap.Tooltip(tooltipTriggerEl)
        })
    });

    document.addEventListener('DOMContentLoaded', function() {
        const boardCarousel = document.querySelector('.board-inner');
        const prevButton = document.getElementById('prevBoard');
        const nextButton = document.getElementById('nextBoard');
        const boardItems = document.querySelectorAll('.board-item');
        const indicators = document.querySelectorAll('.board-indicators button');

        let currentIndex = 0;
        let itemWidth = boardItems[0].offsetWidth;
        const itemsPerView = 4; // Number of items visible at once
        const totalItems = boardItems.length;
        const maxIndex = Math.ceil(totalItems / itemsPerView) - 1;

        // Update item width on window resize
        window.addEventListener('resize', function() {
            itemWidth = boardItems[0].offsetWidth;
            goToSlide(currentIndex);
        });

        // Function to move the carousel
        function goToSlide(index) {
            currentIndex = index;
            const translateValue = -currentIndex * (itemWidth * itemsPerView);
            boardCarousel.style.transform = `translateX(${translateValue}px)`;

            // Update indicators
            indicators.forEach((indicator, i) => {
                if (i === currentIndex) {
                    indicator.classList.remove('bg-secondary');
                    indicator.classList.add('bg-primary');
                } else {
                    indicator.classList.remove('bg-primary');
                    indicator.classList.add('bg-secondary');
                }
            });

            // Disable/enable buttons based on position
            prevButton.disabled = currentIndex === 0;
            prevButton.style.opacity = currentIndex === 0 ? 0.5 : 1;
            nextButton.disabled = currentIndex === maxIndex;
            nextButton.style.opacity = currentIndex === maxIndex ? 0.5 : 1;
        }

        // Next button click
        nextButton.addEventListener('click', function() {
            if (currentIndex < maxIndex) {
                goToSlide(currentIndex + 1);
            }
        });

        // Previous button click
        prevButton.addEventListener('click', function() {
            if (currentIndex > 0) {
                goToSlide(currentIndex - 1);
            }
        });

        // Indicator buttons click
        indicators.forEach((indicator, index) => {
            indicator.addEventListener('click', function() {
                goToSlide(index);
            });
        });

        // Add responsive behavior for mobile
        function updateCarouselForScreenSize() {
            const screenWidth = window.innerWidth;

            if (screenWidth < 576) { // Extra small devices
                itemsPerView = 1;
            } else if (screenWidth < 768) { // Small devices
                itemsPerView = 2;
            } else if (screenWidth < 992) { // Medium devices
                itemsPerView = 3;
            } else { // Large devices
                itemsPerView = 4;
            }

            goToSlide(0); // Reset to first slide with new config
        }

        // Initialize
        updateCarouselForScreenSize();
        window.addEventListener('resize', updateCarouselForScreenSize);

        // Initial setup
        goToSlide(0);
    });
</script>
